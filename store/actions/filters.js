export const FILTER_GREEN = 'FILTER_GREEN';
export const FILTER_ORANGE = 'FILTER_ORANGE';
export const FILTER_RED = 'FILTER_RED';

export const filterGreenBills = () => {
    return {type: FILTER_GREEN}
};

export const filterOrangeBills = () => {
    return {type: FILTER_ORANGE}
}

export const filterRedBills = () => {
    return {type: FILTER_RED}
}